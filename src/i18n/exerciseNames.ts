/**
 * Pure helper. Returns translated display name or canonical English fallback.
 * Safe to call with any object that has `name` (and optionally `id`).
 */
export function translateExerciseName(
  ex: { id?: string; name: string } | null | undefined,
  lang?: string,
): string {
  if (!ex) return '';
  const l = (lang ?? i18n.language ?? 'en').split('-')[0];
  if (l === 'en') return ex.name;
  const map = TRANSLATIONS[l];
  return map?.[ex.name] ?? ex.name;
}

/**
 * Hook variant. Subscribes to language changes so components re-render
 * when the user switches language in Settings.
 */
export function useExerciseName() {
  const { i18n: i } = useTranslation();
  return (ex: { id?: string; name: string } | null | undefined) =>
    translateExerciseName(ex, i.language);
}
