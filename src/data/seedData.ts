function r(name: string, catId: string, opts?: Partial<Exercise>): Exercise {
  return {
    id: `ex-${name.toLowerCase().replace(/[^a-z0-9]+/g, '-')}`,
    name,
    categoryId: catId,
    type: catId === 'cat-cardio' ? 'CARDIO' : 'RESISTANCE',
    setType: catId === 'cat-cardio' ? 'REPS_DISTANCE' : 'WEIGHT_REPS',
    weightUnit: 'kg',
    defaultRepsMin: catId === 'cat-cardio' ? null : 8,
    defaultRepsMax: catId === 'cat-cardio' ? null : 12,
    defaultSets: catId === 'cat-cardio' ? 1 : 3,
    defaultRestSeconds: catId === 'cat-cardio' ? null : 90,
    notes: '',
    isFavorite: false,
    isCustom: false,
    ...opts,
  };
}

